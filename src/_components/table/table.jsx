const tableComponent = ({ TableCellHeaders,rows,allrows,rowsPerPage,page,handleChangePage,handleChangeRowsPerPage }) => {
  const emptyRows = rowsPerPage - rows.length;
  return (
    <Paper className={styles.root}>
      <div className={styles.tableWrapper}>
        <TableMD className={styles.table}>
        <TableHead>
          <TableRow>
            {TableCellHeaders}
          </TableRow>
        </TableHead>
          <TableBody>
            {rows}
            {emptyRows > 0 && (
              <TableRow style={{ height: 48 * emptyRows }}>
                <TableCell colSpan={6} />
              </TableRow>
            )}
          </TableBody>
          <TableFooter>
            <TableRow>
              <TablePagination
                rowsPerPageOptions={[10]}
                colSpan={3}
                count={allrows}
                labelDisplayedRows={({ from, to, count }) => `${from}-${to}  از  ${count}`}
                labelRowsPerPage={'تعداد ردیف در صفحه :'}
                rowsPerPage={rowsPerPage}
                page={page}
                SelectProps={{
                  native: true,
                }}
                onChangePage={handleChangePage}
                onChangeRowsPerPage={handleChangeRowsPerPage}
                ActionsComponent={TablePaginationActionsWrapped}
              />
            </TableRow>
          </TableFooter>
        </TableMD>
      </div>
    </Paper>
  )
};

export const Table = withStyles(styles)(connectTo(state => ({ companyName: state.generic.companyName }), {}, tableComponent))